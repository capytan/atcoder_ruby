require 'rspec'

RSpec.describe 'test' do
  it 'test with "2 6\n1 2 4\n2 2 4\n"' do
    io = IO.popen("ruby abc188/D.rb", "w+")
    io.puts("2 6\n1 2 4\n2 2 4\n")
    io.close_write
    expect(io.readlines.join).to eq("10\n")
  end

  it 'test with "5 1000000000\n583563238 820642330 44577\n136809000 653199778 90962\n54601291 785892285 50554\n5797762 453599267 65697\n468677897 916692569 87409\n"' do
    io = IO.popen("ruby abc188/D.rb", "w+")
    io.puts("5 1000000000\n583563238 820642330 44577\n136809000 653199778 90962\n54601291 785892285 50554\n5797762 453599267 65697\n468677897 916692569 87409\n")
    io.close_write
    expect(io.readlines.join).to eq("163089627821228\n")
  end

  it 'test with "5 100000\n583563238 820642330 44577\n136809000 653199778 90962\n54601291 785892285 50554\n5797762 453599267 65697\n468677897 916692569 87409\n"' do
    io = IO.popen("ruby abc188/D.rb", "w+")
    io.puts("5 100000\n583563238 820642330 44577\n136809000 653199778 90962\n54601291 785892285 50554\n5797762 453599267 65697\n468677897 916692569 87409\n")
    io.close_write
    expect(io.readlines.join).to eq("88206004785464\n")
  end

end
